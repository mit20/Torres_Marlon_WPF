//Calculate how old Sparky the pit bull is in dog years based on his actual age. Dogs age 7 times faster than humans
// so a dog that is 1 year old in human years is 7 years old in “dog years.”  Calculate how old Sparky the pit bull
// is in dog years based on his actual age.

var humanYear = 1; //Given 1 equals a human year.
var dogYears = 7;  //Given that 7 dog years are equal to 1 human year.
var sparkyAge = humanYear * dogYears; //Expression that calculates Sparky's Age.
console.log("Sparky is" + " " + humanYear + " " +  "human years old which is" + " " + sparkyAge +" " + "in dog years.");
